print_and_clear() {
    local text="$1"
    local filter="$2"
    local delay="${3:-1}"
    local lines=$(toilet -f small "$text" | wc -l)
    toilet -f small $filter "$text"
    sleep $delay
    for i in $(seq 1 $lines); do
        tput cuu1
        tput el
    done
}


